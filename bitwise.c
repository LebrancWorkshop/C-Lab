#include <stdio.h>

int main()
{
    printf("Bitwise Operation\n");
    printf("10 & 2 -> %d\n",10 & 2);
    printf("10 | 2 -> %d\n",10 | 2);
    printf("10 ^ 2 -> %d\n",10 ^ 2);
    printf("~10 -> %d\n",~10);
    printf("~2 -> %d\n",~2);
    printf("10 >> 2 -> %d\n",10 >> 2);
    printf("10 << 2 -> %d\n",10 << 2);
    printf("2 << 3 -> %d\n",2 << 3);
    return 0;
}
